function parse(data, out, name) {
  const result = {};
  data.forEach(el => {
    el.forEach((v, i) => {
      if (!result[out[i]]) {
        result[out[i]] = [];
      }
      result[out[i]].push(v.replace(/,|-| |_|%/g, ''));
    });
  });

  fs.writeFile(name, JSON.stringify(result), err => {
    if (err) throw err;
    console.log('complete');
  });
  return result;
}

